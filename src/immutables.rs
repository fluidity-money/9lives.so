pub fn boring_proxy_code(
    addr: Address,
) -> [u8; 20 + concat_arrays_size!(NORMAL_PROXY_BYTECODE_1, NORMAL_PROXY_BYTECODE_2)] {
    concat_arrays!(
        NORMAL_PROXY_BYTECODE_1,
        addr.into_array(),
        NORMAL_PROXY_BYTECODE_2
    )
}

pub fn trading_proxy_code(
    factory: Address,
    is_dpm: bool,
) -> [u8; concat_arrays_size!(TRADING_PROXY_BYTECODE) + (32 * 2)] {
    let mut w = [0u8; 32];
    w[31] = is_dpm as u8;
    concat_arrays!(TRADING_PROXY_BYTECODE, factory.into_word().into(), w)
}

pub fn erc20_proxy_hash(erc20_impl: Address) -> [u8; 32] {
    Keccak256::new()
        .update(&boring_proxy_code(erc20_impl))
        .finalize()
}

pub fn trading_proxy_hash(factory: Address, is_dpm: bool) -> [u8; 32] {
    Keccak256::new()
        .update(&trading_proxy_code(factory, is_dpm))
        .finalize()
}

